#!/usr/bin/env node

/**
 * .orch/cli.js -- Elite Agent Orchestration Engine
 * 
 * Features:
 *   - Smart Workspaces: Local branch management with automatic Worktree fallback.
 *   - Technical Handoffs: File-driven HANDOFF.md protocol for rich documentation.
 *   - Ownership Guardrails: Ensures agents only touch files they are assigned.
 *   - Resource Locking: Prevents port/database collisions between parallel agents.
 *   - Mission Briefing: Injects context directly into the agent's workspace.
 */

import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const ROOT = process.cwd();
const ORCH_DIR = path.join(ROOT, '.orch');
const PLAN_DIR = path.join(ORCH_DIR, 'plan');
const STATUS_FILE = path.join(ORCH_DIR, 'status.json');
const NOTES_DIR = path.join(ORCH_DIR, 'notes');
const LOG_FILE = path.join(ORCH_DIR, 'orch.log');
const WORKTREE_DIR = path.join(ORCH_DIR, 'worktrees');
const ARCHIVE_DIR = path.join(ORCH_DIR, 'archive');

const SYMLINK_DIRS = ['node_modules', '.venv', 'venv', 'vendor', 'target', 'dist'];

// --- UI Symbols ---
const SYM = {
  OK:     '[OK]    ',
  FAIL:   '[FAIL]  ',
  WARN:   '[!]     ',
  NOTE:   '[NOTE]  ',
  START:  '[START] ',
  DONE:   '[DONE]  ',
  TEST:   '[TEST]  ',
  LOCK:   '[LOCK]  ',
  BRIEF:  '[BRIEF] '
};

const COLORS = {
  reset: '\x1b[0m', bold: '\x1b[1m', dim: '\x1b[2m',
  green: '\x1b[32m', blue: '\x1b[34m', yellow: '\x1b[33m',
  red: '\x1b[31m', cyan: '\x1b[36m', grey: '\x1b[90m'
};

const STATUS_COLORS = {
  ready: COLORS.green, in_progress: COLORS.blue,
  complete: COLORS.grey, blocked: COLORS.yellow, failed: COLORS.red
};

// --- Helpers ---

function ts() { return new Date().toISOString().replace(/\.\d+Z$/, 'Z'); }

function log(op, id = '', msg = '') {
  const line = `[${ts()}] ${op.padEnd(10)} ${id.padEnd(16)} ${msg}\n`;
  fs.appendFileSync(LOG_FILE, line);
}

function loadStatus() {
  return JSON.parse(fs.readFileSync(STATUS_FILE, 'utf8'));
}

function saveStatus(data) {
  data.meta.last_updated = ts();
  fs.writeFileSync(STATUS_FILE, JSON.stringify(data, null, 2), 'utf8');
}

function getTask(data, id) {
  if (!data[id]) { console.error(`${SYM.FAIL} Task ${id} not found.`); process.exit(1); }
  return data[id];
}

function getTasks(data) {
  const tasks = {};
  for (const [k, v] of Object.entries(data)) if (k !== 'meta') tasks[k] = v;
  return tasks;
}

function formatDuration(ms) {
  const sec = Math.floor(ms / 1000);
  const min = Math.floor(sec / 60);
  const hr = Math.floor(min / 60);
  if (hr > 0) return `${hr}h ${min % 60}m`;
  return `${min}m ${sec % 60}s`;
}

// --- Elite Features ---

function checkResourceLocks(tasks, newTask) {
  const inProgress = Object.values(tasks).filter(t => t.status === 'in_progress');
  const requested = newTask.resources || [];
  
  for (const t of inProgress) {
    const active = t.resources || [];
    const collision = requested.find(r => active.includes(r));
    if (collision) {
      console.error(`${SYM.LOCK} RESOURCE COLLISION: Task ${t.title} is currently using ${collision}.`);
      console.error(`  Please wait for that task to finish or re-assign resources.`);
      process.exit(1);
    }
  }
}

function createMissionBrief(workDir, task, id, data) {
  const briefPath = path.join(workDir, 'MISSION_BRIEF.md');
  const tasks = getTasks(data);
  
  let upstreamNotes = '';
  (task.depends_on || []).forEach(depId => {
    const noteFile = path.join(NOTES_DIR, `${depId}.md`);
    if (fs.existsSync(noteFile)) {
      upstreamNotes += `### From ${depId}:\n${fs.readFileSync(noteFile, 'utf8')}\n\n`;
    }
  });

  const content = `# MISSION BRIEF: ${id}\n\n` +
    `**Objective:** ${task.title}\n` +
    `**Agent:** ${task.agent}\n` +
    `**DoD:** ${task.definition_of_done}\n\n` +
    `## 1. Authorized Files\n` +
    `You are strictly authorized to edit ONLY these files:\n` +
    (task.output_files?.map(f => `- \`${f}\``).join('\n') || 'None listed') + `\n\n` +
    `## 2. Upstream Intelligence\n` +
    (upstreamNotes || 'No upstream handoffs recorded.\n') +
    `## 3. Required Resources\n` +
    (task.resources?.join(', ') || 'None') + `\n\n` +
    `--- \n*Generated by Agent Orchestration at ${ts()}*`;

  fs.writeFileSync(briefPath, content);
  
  // Also create the HANDOFF template
  const handoffPath = path.join(workDir, 'HANDOFF.md');
  const handoffTemplate = `# HANDOFF: ${id}\n\n## 1. Approach\n(Describe how you solved this task)\n\n` +
    `## 2. Technical Changes\n(List key logic changes or new dependencies)\n\n` +
    `## 3. Interface Updates\n(Crucial: Describe new API shapes, DB columns, or UI components)\n\n` +
    `## 4. Note for Downstream Agents\n(What should the next agent know?)\n`;
  
  if (!fs.existsSync(handoffPath)) fs.writeFileSync(handoffPath, handoffTemplate);
}

function verifyOwnership(workDir, allowedFiles) {
  if (!allowedFiles || allowedFiles.length === 0) return;
  try {
    const status = execSync('git status --porcelain', { cwd: workDir }).toString();
    const changedFiles = status.split('\n').filter(l => l.trim()).map(l => l.slice(3));
    
    for (const file of changedFiles) {
      if (!allowedFiles.includes(file)) {
        console.warn(`${SYM.WARN} OWNERSHIP VIOLATION: You modified \`${file}\` but do not own it.`);
        console.warn(`  Authorized files: ${allowedFiles.join(', ')}`);
        // We don't exit(1) yet to allow for common global files like package.json, 
        // but a "Surgical" mode would block here.
      }
    }
  } catch (e) {}
}

// --- Commands ---

function init() {
  console.log(`${COLORS.bold}Initialising Elite Agent Orchestration...${COLORS.reset}`);
  if (!fs.existsSync(ORCH_DIR)) fs.mkdirSync(ORCH_DIR);
  if (!fs.existsSync(PLAN_DIR)) fs.mkdirSync(PLAN_DIR);
  if (!fs.existsSync(NOTES_DIR)) fs.mkdirSync(NOTES_DIR);
  if (!fs.existsSync(ARCHIVE_DIR)) fs.mkdirSync(ARCHIVE_DIR);

  const seedPlan = path.join(PLAN_DIR, 'AGENT_STATUS.json');
  if (!fs.existsSync(seedPlan)) {
    console.error(`${SYM.FAIL} Create .orch/plan/AGENT_STATUS.json first.`);
    process.exit(1);
  }

  const data = JSON.parse(fs.readFileSync(seedPlan, 'utf8'));
  saveStatus(data);
  console.log(`${SYM.OK} Elite Engine Online.`);

  // Create root wrapper
  const wrapper = `#!/usr/bin/env node\nimport { spawn } from 'child_process';\nimport { fileURLToPath } from 'url';\nimport path from 'path';\nconst __dirname = path.dirname(fileURLToPath(import.meta.url));\nspawn('node', [path.join(__dirname, '.orch', 'cli.js'), ...process.argv.slice(2)], { stdio: 'inherit' });\n`;
  fs.writeFileSync(path.join(ROOT, 'orch'), wrapper);
  fs.chmodSync(path.join(ROOT, 'orch'), 0o755);
}

function startTask(id, forceWorktree = false) {
  const data = loadStatus();
  const tasks = getTasks(data);
  const task = getTask(data, id);

  checkResourceLocks(tasks, task);

  const inProgress = Object.entries(tasks).filter(([tid, t]) => t.status === 'in_progress');
  const useWorktree = forceWorktree || inProgress.length > 0;
  const branch = `${task.agent}/${id}`;
  const workDir = useWorktree ? path.join(WORKTREE_DIR, id) : ROOT;

  console.log(`${SYM.START} Activating ${id} (${useWorktree ? 'Worktree' : 'Local'})...`);

  try {
    if (useWorktree) {
      execSync(`git worktree add ${branchExists(branch) ? '' : '-b ' + branch} "${workDir}" ${branchExists(branch) ? branch : ''}`, { stdio: 'inherit' });
      SYMLINK_DIRS.forEach(dir => {
        const src = path.join(ROOT, dir);
        const dest = path.join(workDir, dir);
        if (fs.existsSync(src) && !fs.existsSync(dest)) fs.symlinkSync(path.relative(path.dirname(dest), src), dest, 'junction');
      });
      task.mode = 'worktree';
    } else {
      execSync(`git checkout ${branchExists(branch) ? branch : '-b ' + branch}`, { stdio: 'inherit' });
      task.mode = 'local';
    }

    createMissionBrief(workDir, task, id, data);
    task.status = 'in_progress';
    task.started_at = ts();
    saveStatus(data);
    
    console.log(`\n${SYM.BRIEF} MISSION_BRIEF.md created in workspace.`);
    console.log(`${SYM.NOTE} Use HANDOFF.md to document your work.`);
  } catch (e) { console.error(`${SYM.FAIL} Start failed: ${e.message}`); process.exit(1); }
}

function doneTask(id) {
  const data = loadStatus();
  const task = getTask(data, id);
  const workDir = task.mode === 'worktree' ? path.join(WORKTREE_DIR, id) : ROOT;

  console.log(`${SYM.DONE} Validating ${id}...`);

  // 1. Verify Handoff File
  const handoffPath = path.join(workDir, 'HANDOFF.md');
  if (!fs.existsSync(handoffPath)) {
    console.error(`${SYM.FAIL} Missing HANDOFF.md. Documentation is required for handoff.`);
    process.exit(1);
  }

  // 2. Surgical Ownership Check
  verifyOwnership(workDir, task.output_files);

  // 3. DoD Verification (Tests)
  if (task.test_command) {
    console.log(`${SYM.TEST} Running Verification...`);
    try { execSync(task.test_command, { cwd: workDir, stdio: 'inherit' }); } 
    catch (e) { console.error(`${SYM.FAIL} DoD Verification Failed.`); process.exit(1); }
  }

  // 4. Capture Handoff and Finalise
  const handoffContent = fs.readFileSync(handoffPath, 'utf8');
  const noteFile = path.join(NOTES_DIR, `${id}.md`);
  fs.appendFileSync(noteFile, `\n---\n### [${ts()}] ${task.agent} completed task:\n${handoffContent}\n`);

  try {
    if (task.mode === 'worktree') {
      execSync(`git -C "${workDir}" add . && git -C "${workDir}" commit -m "[${id}] ${task.title}"`, { stdio: 'inherit' });
      execSync(`git worktree remove "${workDir}"`, { stdio: 'inherit' });
    } else {
      execSync(`git add . && git commit -m "[${id}] ${task.title}"`, { stdio: 'inherit' });
    }
    fs.rmSync(path.join(workDir, 'MISSION_BRIEF.md'), { force: true });
    
    task.status = 'complete';
    task.completed_at = ts();
    if (task.started_at) task.duration_ms = new Date(task.completed_at) - new Date(task.started_at);
    
    resolve(data);
    saveStatus(data);
    console.log(`${SYM.OK} Task complete. Lead time: ${formatDuration(task.duration_ms)}`);
  } catch (e) { console.error(`${SYM.FAIL} Finalisation failed: ${e.message}`); process.exit(1); }
}

function branchExists(branch) {
  try { execSync(`git rev-parse --verify ${branch}`, { stdio: 'ignore' }); return true; } catch (e) { return false; }
}

function dashboard(data) {
  const tasks = getTasks(data);
  const now = new Date();
  
  console.log(`\n${COLORS.bold}${data.meta?.project} Status${COLORS.reset}\n${SEP_LIGHT}`);
  
  Object.entries(tasks).forEach(([id, t]) => {
    const color = STATUS_COLORS[t.status] || '';
    let stallWarning = '';
    
    if (t.status === 'in_progress' && t.started_at && t.estimated_duration) {
      const elapsed = (now - new Date(t.started_at)) / (1000 * 60 * 60);
      if (elapsed > parseFloat(t.estimated_duration)) stallWarning = ` ${COLORS.red}[STALLED]${COLORS.reset}`;
    }

    console.log(`  ${color}[${t.status.toUpperCase().padEnd(11)}]${COLORS.reset} [${t.agent.padEnd(8)}] ${id}: ${t.title}${stallWarning}`);
  });
  console.log();
}

// --- Main ---
const args = process.argv.slice(2);
const getArg = (f) => args.includes(f) ? args[args.indexOf(f) + 1] : null;

if (args.includes('--init')) init();
else if (args.includes('--start')) startTask(getArg('--start'), args.includes('--worktree'));
else if (args.includes('--done')) doneTask(getArg('--done'));
else dashboard(loadStatus());
